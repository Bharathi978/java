package org.get;

public class GetterSample {

	int id;
	String name;
	Long phone;
	
	
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Long getPhone() {
		return phone;
	}
	public void setPhone(Long phone) {
		this.phone = phone;
	}
	public GetterSample(int id, String name, Long phone) {
		
		this.id = id;
		this.name = name;
		this.phone = phone;
	}
	
	
	
	
	
	

	
	
	
	
	
	
	
	
	
	
	
	
	
}
